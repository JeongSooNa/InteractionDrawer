class AtomInfo {
    static maxPossibleBonds(element) {
        const upcaseEl = element.toUpperCase();
        if (!this.maxBonds.hasOwnProperty(upcaseEl)) {
            return -1;
        }
        return this.maxBonds[upcaseEl];
    }
}

